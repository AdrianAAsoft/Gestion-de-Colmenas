    /**
     * Registra una nueva colmena solicitando los datos al usuario.
     */
    public static void registrarColmena() {
        Utils.delayPrint("\nüêù REGISTRO DE NUEVA COLMENA", 500);

        try {
            String id = solicitarIdUnico();
            String ubicacion = Utils.solicitarCampo("Ingrese ubicaci√≥n de la colmena: ");
            String estadoSalud = solicitarEstadoSalud();
            String tipo = Utils.solicitarCampo("Ingrese tipo de colmena (Ej: Langstroth, Warre, Top-Bar): ");
            byte cantidadAbejas = Utils.solicitarByteEnRango("Cantidad de abejas (0‚Äì25): ", (byte) 0, (byte) 25);
            float produccionMiel = Utils.solicitarFloatMin("Producci√≥n estimada de miel (kg): ", 0f);

            Colmena nuevaColmena = new Colmena(id, ubicacion, tipo, estadoSalud, cantidadAbejas, produccionMiel);

            DatosApicola.getInstancia().agregarColmena(nuevaColmena);

            Utils.delayPrint("‚úÖ Colmena registrada correctamente.", 500);
        } catch (Exception e) {
            Utils.delayPrint("‚ùå Error al registrar la colmena: " + e.getMessage(), 500);
        }
    }

    /**
     * Permite al usuario registrar inspecciones de colmenas.
     */
    public static void registrarInspeccion() {
        String modo = Utils.solicitarCampo("""
        üîç ¬øDesea inspeccionar una sola colmena o todas?
        Escriba: 'una' o 'todas'
        üëâ """).trim().toLowerCase();

        switch (modo) {
            case "una" -> inspeccionarUnaColmena();
            case "todas" -> inspeccionarMultiplesColmenas();
            default -> Utils.delayPrint("‚ùå Opci√≥n no v√°lida. Intente de nuevo.", 500);
        }
    }

