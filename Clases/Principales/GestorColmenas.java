    /**
     * Solicita al usuario un estado de salud v√°lido para la colmena.
     */
    private static String solicitarEstadoSalud() {
        final String mensaje = """
        Estado de Salud:
        ‚îú‚îÄ En plenitud
        ‚îú‚îÄ Zumbido estable
        ‚îî‚îÄ Colmena en riesgo
        üëâ Ingresa una opci√≥n:""";

        while (true) {
            String input = Utils.solicitarCampo(mensaje).trim().toLowerCase();
            if (input.matches("en plenitud|zumbido estable|colmena en riesgo")) {
                return capitalize(input);
            }
            Utils.delayPrint("‚ùå Opci√≥n inv√°lida. Intenta de nuevo.", 500);
        }
    }

    /**
     * Solicita al usuario un ID √∫nico de colmena siguiendo el formato COL###.
     */
    private static String solicitarIdUnico() {
        while (true) {
            String id = Utils.solicitarCampo("Ingrese ID de la colmena (formato COL###): ").toUpperCase();

            if (!id.matches("COL\\d{3}")) {
                Utils.delayPrint("‚ùå Formato inv√°lido. Debe ser COL seguido de 3 d√≠gitos (ej. COL001).", 500);
                continue;
            }

            if (Utils.idExiste(DatosApicola.getInstancia().obtenerColmenas(), id)) {
                Utils.delayPrint("‚ùå El ID " + id + " ya est√° registrado.\n", 500);
                continue;
            }

            return id;
        }
    }

    /**
     * Busca una colmena por su ID en el sistema.
     */
    private static Colmena buscarColmena(String id) {
        return DatosApicola.getInstancia().obtenerColmenas().stream()
                .filter(c -> c.getId().equals(id))
                .findFirst()
                .orElse(null);
    }

    /**
     * Capitaliza el primer car√°cter de un texto.
     */
    private static String capitalize(String text) {
        return text.substring(0, 1).toUpperCase() + text.substring(1);
    }
